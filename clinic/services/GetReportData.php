                            <tr>
                                <td><?= isset($visit['visit_time']) ? date('h:i A', strtotime($visit['visit_time'])) : (isset($visit['visit_date']) ? date('h:i A', strtotime($visit['visit_date'])) : 'N/A') ?></td>
