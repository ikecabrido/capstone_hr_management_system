<?php
session_start();
require_once "../../auth/auth_check.php";
require_once "../../auth/database.php";
require_once "ClinicReportService.php";

$database = Database::getInstance();
$db = $database->getConnection();

if ($db === null) {
    echo '<div class="alert alert-danger">Database connection failed</div>';
    exit;
}

$reportService = new ClinicReportService($db);
$report_id = $_GET['report_id'] ?? '';

if (empty($report_id)) {
    echo '<div class="alert alert-warning">Report ID is required</div>';
    exit;
}

$report = $reportService->getReport($report_id);

if (!$report) {
    echo '<div class="alert alert-danger">Report not found</div>';
    exit;
}

$report_data = json_decode($report['report_data'], true);
?>

