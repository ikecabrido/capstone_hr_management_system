        </div>
        <div class="col-sm-6 text-sm-right">
            <p class="mb-1 text-muted"><strong>Date Generated:</strong> <?= date('M d, Y h:i A', strtotime($report['created_at'])) ?></p>
            <p class="mb-0 text-muted"><strong>Period:</strong> <?= date('M d, Y', strtotime($report['start_date'])) ?> to <?= date('M d, Y', strtotime($report['end_date'])) ?></p>
        </div>
    </div>

