                            <div class="card-body py-3 px-2 text-center">
                                <small class="text-uppercase text-muted d-block mb-1"><?= ucwords(str_replace('_', ' ', $key)) ?></small>
                                <span class="h5 font-weight-bold mb-0"><?= is_numeric($value) ? number_format($value) : htmlspecialchars($value) ?></span>
                            </div>
